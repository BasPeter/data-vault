import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(fileURLToPath(new URL("..", import.meta.url)));
const outputPath = resolve(root, "electron/app-changelog.generated.ts");

function git(args) {
  return execFileSync("git", args, { cwd: root, encoding: "utf8" }).trim();
}

function packageMetadata() {
  const pkg = JSON.parse(readFileSync(resolve(root, "package.json"), "utf8"));
  const repositoryUrl = typeof pkg.repository === "string"
    ? pkg.repository
    : pkg.repository && typeof pkg.repository.url === "string"
      ? pkg.repository.url
      : undefined;
  return { version: pkg.version, repositoryUrl };
}

function versionKey(tag) {
  return tag.replace(/^v/, "").split(".").map((part) => Number.parseInt(part, 10));
}

function compareVersions(left, right) {
  const a = versionKey(left);
  const b = versionKey(right);
  for (let i = 0; i < Math.max(a.length, b.length); i += 1) {
    const diff = (a[i] ?? 0) - (b[i] ?? 0);
    if (diff !== 0) return diff;
  }
  return left.localeCompare(right);
}

function commitsForRange(range) {
  const raw = git(["log", "--pretty=format:%H%x1f%h%x1f%s", range]);
  if (!raw) return [];
  return raw.split("\n").map((line) => {
    const [hash, shortHash, subject] = line.split("\x1f");
    return { hash, shortHash, subject };
  });
}

const { version: packageVersion, repositoryUrl } = packageMetadata();
const tags = git(["tag", "--list", "v[0-9]*", "--sort=version:refname"])
  .split("\n")
  .map((tag) => tag.trim())
  .filter(Boolean)
  .sort(compareVersions);

const releases = [];
const packageTag = `v${packageVersion}`;
const latestTag = tags.at(-1);

if (packageVersion && !tags.includes(packageTag)) {
  releases.push({
    version: packageVersion,
    date: git(["show", "-s", "--format=%cI", "HEAD"]),
    commits: commitsForRange(latestTag ? `${latestTag}..HEAD` : "HEAD"),
  });
}

releases.push(...tags.map((tag, index) => {
  const previous = tags[index - 1];
  const range = previous ? `${previous}..${tag}` : tag;
  return {
    version: tag.replace(/^v/, ""),
    date: git(["log", "-1", "--format=%cI", tag]),
    commits: commitsForRange(range),
  };
}).reverse());

const changelog = {
  generatedAt: git(["show", "-s", "--format=%cI", "HEAD"]),
  repositoryUrl,
  releases,
};

const content = `// Generated by scripts/generate-changelog.mjs. Do not edit by hand.
import type { AppChangelog } from "../src/types";

export const APP_CHANGELOG = ${JSON.stringify(changelog, null, 2)} satisfies AppChangelog;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, content);
